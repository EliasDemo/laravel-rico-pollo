        <div class="collapse info-topic-content" id="s42">

            <p>
                Corporación Rico S.A.C. está en la fase de consolidación de su infraestructura
                digital para asegurar la calidad, optimizar la cadena de suministro integrada
                y protegerse contra los riesgos de ineficiencia y mercado (Amenazas A1, A9).
                La tecnología es el principal habilitador para cumplir con los objetivos
                estratégicos 4.3 (Potenciar el uso de tecnologías de información) y 3.2
                (Asegurar la calidad e inocuidad alimentaria) de nuestro BSC.
            </p>

            <div class="info-subtopic-box">
                <div class="info-subtopic-title">4.2.1. Uso de Sistemas de Planificación (ERP) y Análisis de Datos (BI)</div>

                <p class="info-small-note">
                    La iniciativa estratégica clave para Rico S.A.C. es acelerar la implementación
                    del BI y ERP (DA 4). Esto se logra mediante:
                </p>

                <ul class="info-small-note mb-0">
                    <li>
                        <strong>Integración de la Cadena:</strong>
                        El ERP unifica la información de granja, planta de beneficio y tiendas,
                        permitiendo el control financiero y operativo de la Integración Vertical (F5)
                        en tiempo real.
                    </li>

                    <li>
                        <strong>Análisis Predictivo de Inventarios:</strong>
                        El BI utiliza datos históricos de consumo y variaciones del tipo de cambio (A1)
                        para prever las necesidades de maíz y soya (A9), permitiendo compras estratégicas
                        y mitigando el riesgo de desabastecimiento.
                    </li>

                    <li>
                        <strong>Automatización de Informes:</strong>
                        Generación automática de reportes de calidad e inocuidad (F6), reduciendo la
                        carga laboral y asegurando el cumplimiento de regulaciones sanitarias (A2).
                    </li>
                </ul>
            </div>

            <div class="info-subtopic-box mt-2">
                <div class="info-subtopic-title">4.2.2. Tecnología para Trazabilidad y Calidad (IoT)</div>

                <ul class="info-small-note mb-0">

                    <li>
                        <strong>Monitoreo en Cadena de Frío:</strong>
                        Implementación de sensores IoT en almacenes y transporte (Logística de Frío –
                        Estrategia FO 4) para registrar continuamente la temperatura, asegurando la
                        frescura (O2) y calidad del pollo desde la planta hasta la tienda.
                    </li>

                    <li>
                        <strong>Sistemas de Pesaje Automatizado:</strong>
                        Integración de los equipos de postproducción (F4) con el ERP para garantizar
                        precisión del empaque y reducir mermas (Objetivo 3.2).
                    </li>

                </ul>
            </div>

            <div class="info-subtopic-box mt-2">
                <div class="info-subtopic-title">4.2.3. Ciberseguridad y Plataformas Digitales</div>

                <ul class="info-small-note mb-0">

                    <li>
                        <strong>Protección de Datos:</strong>
                        Firewalls avanzados y sistemas de cifrado para proteger información financiera
                        y datos de clientes de la plataforma de e-commerce.
                    </li>

                    <li>
                        <strong>Autenticación y Accesibilidad:</strong>
                        Sistemas de acceso remoto seguro (Cloud Computing) que permiten a Gerencia
                        General y Jefaturas monitorear operación y calidad desde cualquier lugar.
                    </li>

                    <li>
                        <strong>Monitoreo Continuo (24/7):</strong>
                        Herramientas de vigilancia digital que detectan fallos e intrusiones en tiempo
                        real, protegiendo sistemas críticos de producción y venta.
                    </li>

                </ul>
            </div>

