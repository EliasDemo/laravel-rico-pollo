                </li>

                <li>
                    <strong>Reuniones Estratégicas:</strong>
                    Cadencia mensual/trimestral para revisar desempeño estratégico y ajustar
                    iniciativas.
                </li>

                <li>
                    <strong>Evaluación del Desempeño:</strong>
                    Las jefaturas deben ser evaluadas según NPS (2.1) y penetración en Lima (2.4).
