        <div class="collapse info-topic-content" id="s43">

            <h4 class="info-subtopic-title">4.3.1. Dirección Estratégica</h4>

            <p class="info-small-note">
                La Dirección Estratégica en Corporación Rico S.A.C. se enfoca en asegurar que la
                visión a largo plazo se traduzca en acciones diarias medibles (BSC). Su enfoque se basa en:
            </p>

            <ul class="info-small-note">
                <li>
                    <strong>Orientación al Riesgo y Cumplimiento:</strong>
                    Prioriza iniciativas DA y FA para mitigar riesgos como el tipo de cambio (A1)
                    y regulaciones sanitarias (A2).
                </li>

                <li>
                    <strong>Gestión Basada en el Desempeño:</strong>
                    Las metas del BSC permiten asignación de recursos y evaluación de gerencias.
                </li>

                <li>
                    <strong>Foco en Valor Diferenciado:</strong>
                    Impulsa iniciativas FO, como Digitalización de Calidad (FO 2) y Líneas Saludables (FO 5).
                </li>
            </ul>

            <h4 class="info-subtopic-title mt-3">4.3.2. Rol del Liderazgo</h4>

            <ul class="info-small-note">
                <li>
                    <strong>Liderazgo de Cumplimiento (Top-Down):</strong>
                    Responsable de implementar el área de Compliance (DA 1) y cumplir estándares
                    de Inocuidad (A2) y Bienestar Animal (A8).
                </li>

                <li>
                    <strong>Liderazgo de Ejecución (Gerencial):</strong>
                    Las jefaturas motivan al personal operativo (F8) para que la capacitación (FO 1)
                    impacte directamente en eficiencia (IEA) y calidad (F6).
                </li>

                <li>
                    <strong>Liderazgo de Cambio (Innovación):</strong>
                    La Gerencia Comercial lidera Tienda Virtual (DO 2) y Líneas Saludables (FO 5).
                </li>
            </ul>

            <h4 class="info-subtopic-title mt-3">4.3.3. Motivación</h4>

            <ul class="info-small-note">
                <li>
                    Vínculo entre capacitación técnica (4.1) y competencias en inocuidad (F1)
                    y trazabilidad (O3).
                </li>

                <li>
                    Incentivos vinculados a indicadores del BSC como reducción de merma (3.2)
                    y aumento del NPS (2.1).
                </li>

                <li>
                    Necesidad de comunicar misión y visión para corregir debilidad (D2) y
                    reforzar propósito en el personal.
                </li>
            </ul>

        </div>

