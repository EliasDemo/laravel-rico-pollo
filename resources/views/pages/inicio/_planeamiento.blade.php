<div class="collapse info-topic-content" id="s22">

    <p>
        La estructura organizacional de Corporación Rico S.A.C. responde al modelo
        de una empresa agroindustrial integrada, donde la producción, la logística,
        la comercialización y la gestión administrativa funcionan bajo un sistema
        coordinado. La organización combina un núcleo corporativo en Lima con una
        estructura operativa robusta en Arequipa y regiones del sur del Perú.
    </p>

    <div class="info-subtopic-box">
        <div class="info-subtopic-title">2.2.1. Niveles jerárquicos</div>

        <ul class="info-small-note mb-0">
            <li>
                <strong>Cima estratégica:</strong>
                integrada por la Presidencia del Directorio y la Gerencia General,
                responsables de la dirección corporativa, las políticas internas,
                el planeamiento financiero y el posicionamiento del grupo.
            </li>

            <li>
                <strong>Gerencias corporativas:</strong>
                Finanzas, Comercial, Producción, Calidad, Logística y Recursos
                Humanos. Estas áreas establecen lineamientos transversales para
                todas las unidades del grupo, asegurando coherencia y eficiencia.
            </li>

            <li>
                <strong>Mandos medios:</strong>
