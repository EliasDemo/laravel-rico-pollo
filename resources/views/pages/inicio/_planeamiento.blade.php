    {{-- 2.4.1 Implementación de sistemas de calidad --}}
    <div class="info-subtopic-box">
        <div class="info-subtopic-title">2.4.1. Implementación de sistemas de calidad</div>

        <ul class="info-small-note mb-0">
            <li>
                <strong>Cumplimiento normativo sanitario:</strong>
                aplicación rigurosa de las normas emitidas por SENASA y DIGESA,
                especialmente en faenado, transporte, almacenamiento y venta.
            </li>

            <li>
                <strong>HACCP – Sistema preventivo:</strong>
                implementación de Análisis de Peligros y Puntos Críticos de
                Control en plantas industriales para anticipar riesgos,
                garantizar inocuidad y reducir al mínimo las no conformidades.
            </li>

            <li>
                <strong>Buenas Prácticas de Manufactura (BPM):</strong>
                protocolos estrictos en higiene, limpieza, desinfección, uso de
                EPP, lavado de manos, ingreso a zonas de proceso y control de
                contaminación cruzada.
            </li>

            <li>
                <strong>Estándares de proveedores:</strong>
                verificación continua de calidad en insumos críticos (maíz duro
                amarillo, soya, premezclas, aditivos), asegurando nutrición
                adecuada y uniforme para la crianza.
            </li>

            <li>
                <strong>Control veterinario:</strong>
                supervisión sanitaria permanente en crianza, manejo de
                medicamentos permitidos y registro de lotes.
            </li>
        </ul>
    </div>

    {{-- 2.4.2 Tecnologías para el manejo de la calidad --}}
    <div class="info-subtopic-box mt-2">
        <div class="info-subtopic-title">2.4.2. Tecnologías para el manejo de la calidad</div>

        <ul class="info-small-note mb-0">
            <li>
                <strong>Monitoreo de cadena de frío:</strong>
                sensores digitales, termógrafos y registros automáticos en
                cámaras de almacenamiento y unidades de transporte.
                Si la temperatura sale de rango, el lote es puesto en cuarentena
                y evaluado por control de calidad.
            </li>

            <li>
                <strong>Sistemas de trazabilidad:</strong>
                identificación de cada lote desde la granja de origen hasta la
                tienda donde se comercializa. Permite investigar incidencias,
                retirar productos y garantizar transparencia al consumidor.
            </li>

            <li>
                <strong>Laboratorios internos:</strong>
                análisis microbiológicos rápidos para liberar partidas,
                verificación de superficie, hisopados, análisis de agua,
                controles de pH y recuento de microorganismos.
            </li>

            <li>
                <strong>Automatización y control:</strong>
                maquinaria de trozado automático, escaldadoras, túneles de
                enfriamiento, y sistemas de pesaje calibrados para asegurar
                uniformidad y precisión operativa.
            </li>
        </ul>
    </div>

