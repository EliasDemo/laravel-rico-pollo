    {{-- 3.2.1 Uso de Sistemas ERP y BI --}}
    <div class="info-subtopic-box">
        <div class="info-subtopic-title">3.2.1. Uso de Sistemas de Planificación (ERP) y Análisis de Datos (BI)</div>

        <p class="info-small-note">
            La iniciativa estratégica clave para Rico S.A.C. es acelerar la implementación
            del BI y ERP (DA 4). Esto se logra mediante:
        </p>

        <ul class="info-small-note mb-0">

            <li>
                <strong>Integración de la cadena:</strong>
                El ERP unifica la información de granja, planta de beneficio y tiendas,
                permitiendo controlar la Integración Vertical (F5) en tiempo real.
            </li>

            <li>
                <strong>Análisis predictivo de inventarios:</strong>
                El BI utiliza datos históricos y variaciones del tipo de cambio (A1) para
                prever necesidades de maíz y soya (A9), permitiendo compras estratégicas.
            </li>

            <li>
                <strong>Automatización de informes:</strong>
                Generación automática de reportes de calidad e inocuidad (F6), reduciendo
                carga operativa y asegurando cumplimiento de regulaciones sanitarias (A2).
            </li>

        </ul>
    </div>

    {{-- 3.2.2 Tecnología para trazabilidad y calidad --}}
    <div class="info-subtopic-box mt-2">
        <div class="info-subtopic-title">3.2.2. Tecnología para Trazabilidad y Calidad (IoT)</div>

        <ul class="info-small-note mb-0">

            <li>
                <strong>Monitoreo en cadena de frío:</strong>
                Sensores IoT instalados en almacenes y transporte (Logística de Frío –
                Estrategia FO 4) registran temperatura constante para asegurar frescura
                (O2) y calidad desde planta hasta tienda.
            </li>

            <li>
                <strong>Sistemas de pesaje automatizado:</strong>
                Equipos de postproducción (F4) integrados con el ERP garantizan precisión
                en el empaque y reducen mermas (Objetivo 3.2).
            </li>

        </ul>
    </div>

    {{-- 3.2.3 Ciberseguridad y plataformas digitales --}}
    <div class="info-subtopic-box mt-2">
        <div class="info-subtopic-title">3.2.3. Ciberseguridad y Plataformas Digitales</div>

        <ul class="info-small-note mb-0">

            <li>
                <strong>Protección de datos:</strong>
                Implementación de firewalls avanzados y sistemas de cifrado para proteger
                información financiera y datos de clientes de la tienda virtual
                (Estrategia DO 2).
            </li>

            <li>
                <strong>Autenticación y accesibilidad:</strong>
                Sistemas de acceso remoto seguro (Cloud Computing) permiten a Gerencia
